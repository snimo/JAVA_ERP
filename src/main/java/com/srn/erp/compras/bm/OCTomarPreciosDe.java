package com.srn.erp.compras.bm;

import framework.request.bl.Utils.BaseException;
import framework.request.bl.Utils.HashTableDatos;

public class OCTomarPreciosDe  {

  public static final String ULTOC = "ULTOC";
  public static final String COTIZ = "COTIZ";
  public static final String ACUERDO = "ACUERDO";
  public static final String LISTA = "LISTA";
  public static final String INGRESO = "INGRESO";
  public static final String LP_OC = "LPOC";
  
  public static HashTableDatos getTomarPreciosDe() throws BaseException {
  	HashTableDatos tomarPrecios = new HashTableDatos();
  	tomarPrecios.put(ULTOC,"Ultima O.C.");
  	tomarPrecios.put(COTIZ,"Presup. Proveedor");
  	tomarPrecios.put(ACUERDO,"Acuerdo Precios");
  	tomarPrecios.put(LISTA,"Lista Precios");
    tomarPrecios.put(INGRESO,"Ingreso Precios");
    tomarPrecios.put(LP_OC,"L.P. / O.C.");
    return tomarPrecios;
  }
  
  
}
