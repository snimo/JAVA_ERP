package com.srn.erp.conciTarjeta.bm;

import framework.request.bl.Utils.HashTableDatos;

public class TipoArchivoConciliacion {
	
	public static final String TA_VISA = "VISA";
	public static final String TA_MASTER = "MASTER";
	public static final String TA_AME = "AME";
	
	public static HashTableDatos getTiposArchivos() {
		HashTableDatos condiciones = new HashTableDatos();
		condiciones.put(TA_VISA, "Visa");
		condiciones.put(TA_MASTER, "Master");
		condiciones.put(TA_AME, "American Express");
		return condiciones;
	}
	
	
}
