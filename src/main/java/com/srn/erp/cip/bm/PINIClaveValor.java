package com.srn.erp.cip.bm;

public class PINIClaveValor {
	private String clave;
	private String valor;
	
	public PINIClaveValor(String aClave,String aValor) {
		
		this.clave = aClave;
		this.valor = aValor;
	}
	
	public String getClave() {
		return this.clave;
	}
	
	public String getValor() {
		return this.valor;
	}
}
