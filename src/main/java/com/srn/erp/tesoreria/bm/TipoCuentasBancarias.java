package com.srn.erp.tesoreria.bm;

import java.util.Hashtable;

public class TipoCuentasBancarias  {

  public static String CAJA_AHORRO = "CA";
  public static String CTA_CTE = "CC";
  public static String PLAZO_FIJO = "PF";

  public Hashtable tipos = new Hashtable();

  public TipoCuentasBancarias() {
    tipos.put(CAJA_AHORRO,"Caja de Ahorro");
    tipos.put(CTA_CTE,"Cuenta Corriente");
    tipos.put(PLAZO_FIJO,"Plazo Fijo");
  }

  public  Hashtable getTipos() {
    return tipos;
  }


}
